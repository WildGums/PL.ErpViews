CREATE VIEW [dbo].[DemandOrders]
AS
SELECT        dbo.SorMaster.SalesOrder AS OrderNumber, dbo.SorDetail.SalesOrderLine AS LineReference, dbo.SorDetail.MStockCode AS PartNumber, dbo.SorDetail.MShipQty + dbo.SorDetail.MBackOrderQty AS Quantity, 
                         dbo.SorDetail.MLineShipDate AS DueDate, '' AS Priority, '' AS Contact, 'Sales' AS DemandType
FROM            dbo.SorMaster INNER JOIN
                         dbo.SorDetail ON dbo.SorMaster.SalesOrder = dbo.SorDetail.SalesOrder
WHERE        (dbo.SorMaster.OrderStatus <> '*') AND (dbo.SorMaster.OrderStatus <> '\') AND (dbo.SorMaster.OrderStatus <> '9') AND (dbo.SorDetail.LineType = '1') AND 
                         (dbo.SorDetail.MShipQty + dbo.SorDetail.MBackOrderQty > 0) AND (dbo.SorDetail.MStockCode = 'B100')

GO


CREATE VIEW [dbo].[ResourceGroupResources]
AS
SELECT        TOP (100) PERCENT dbo.BomWorkCentre.WorkCentre AS ResourceGroup, dbo.BomMachine.Description AS Resource
FROM            dbo.BomWorkCentre LEFT OUTER JOIN
                         dbo.BomMachine ON dbo.BomWorkCentre.WorkCentre = dbo.BomMachine.WorkCentre
ORDER BY Resource

GO


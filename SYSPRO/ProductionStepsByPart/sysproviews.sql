CREATE VIEW [dbo].[ProductionSteps]
AS
SELECT        dbo.BomOperations.StockCode AS PartNumber, dbo.BomOperations.Operation AS OperationNumber, dbo.BomWorkCentre.Description + '-' + dbo.BomOperations.StockCode AS OperationName, 
                         dbo.BomWorkCentre.WorkCentre AS ResourceGroup, '' AS RequiredResource, RIGHT('00' + CONVERT(varchar(2), FLOOR(dbo.BomOperations.ISetUpTime)), 2) + ':' + RIGHT('00' + CONVERT(varchar(2), 
                         FLOOR((dbo.BomOperations.ISetUpTime - FLOOR(dbo.BomOperations.ISetUpTime)) * 60)), 2) + ':' + RIGHT('00' + CONVERT(varchar(2), 
                         FLOOR((dbo.BomOperations.ISetUpTime - FLOOR(dbo.BomOperations.ISetUpTime)) * 60 - FLOOR((dbo.BomOperations.ISetUpTime - FLOOR(dbo.BomOperations.ISetUpTime)) * 60)) * 60), 2) AS SetupDuration, 
                         'TimePerItem' AS ProcessDurationType, 'Hour' AS ProcessTimePeriod, dbo.BomOperations.IRunTime AS ProcessValue, '' AS MaxIdleDuration, RIGHT('00' + CONVERT(varchar(2), 
                         FLOOR(dbo.BomOperations.IStartupTime)), 2) + ':' + RIGHT('00' + CONVERT(varchar(2), FLOOR((dbo.BomOperations.IStartupTime - FLOOR(dbo.BomOperations.IStartupTime)) * 60)), 2) 
                         + ':' + RIGHT('00' + CONVERT(varchar(2), FLOOR((dbo.BomOperations.IStartupTime - FLOOR(dbo.BomOperations.IStartupTime)) 
                         * 60 - FLOOR((dbo.BomOperations.IStartupTime - FLOOR(dbo.BomOperations.IStartupTime)) * 60)) * 60), 2) AS WarmUpPeriod
FROM            dbo.BomOperations LEFT OUTER JOIN
                         dbo.BomWorkCentre ON dbo.BomOperations.WorkCentre = dbo.BomWorkCentre.WorkCentre LEFT OUTER JOIN
                         dbo.BomMachine ON dbo.BomOperations.IMachine = dbo.BomMachine.Machine
WHERE        (dbo.BomOperations.Route = '0')

GO


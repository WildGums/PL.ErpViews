CREATE VIEW [dbo].[Resources]
AS
SELECT DISTINCT Description AS Name, 'TRUE' AS IsEnabled, ROW_NUMBER() OVER (ORDER BY Description) AS DisplayPosition
FROM            dbo.BomMachine


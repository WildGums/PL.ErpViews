CREATE VIEW [dbo].[BillOfMaterials]
AS
SELECT        ParentPart AS PartNumber, OperationOffset AS OperationNumber, Component AS RequiredPartNumber, QtyPer AS RequiredQuantity, 'TRUE' AS IgnoreShortages
FROM            dbo.BomStructure
WHERE        (Route = '0') AND (OperationOffset > 0)

GO

